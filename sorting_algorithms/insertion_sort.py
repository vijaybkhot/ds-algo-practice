def test_ins_sort(ins_sort_test):
    array_1 = [1232, 1231, 2321, 53123, 63123]
    sorted_array = ins_sort_test(array_1)
    print("Sorted array is:", sorted_array)

    # Test case 1: Empty list
    arr = []
    assert ins_sort_test(arr) == []

    # Test case 2: Already sorted list
    arr = [1, 2, 3, 4, 5]
    assert ins_sort_test(arr) == [1, 2, 3, 4, 5]

    # Test case 3: Reversed list
    arr = [5, 4, 3, 2, 1]
    assert ins_sort_test(arr) == [1, 2, 3, 4, 5]

    # Test case 4: Random list
    arr = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]
    assert ins_sort_test(arr) == [1, 1, 2, 3, 3, 4, 5, 5, 5, 6, 9]

    # Test case 5: List with duplicate elements
    arr = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5, 5]
    assert ins_sort_test(arr) == [1, 1, 2, 3, 3, 4, 5, 5, 5, 5, 6, 9]

    print("All test cases passed!")


