def ins_sort(array):
    for num in range(len(array) - 1):
        key = array[num + 1]
        space = num + 1
        while space > 0 and array[space - 1] > key:
            array[space] = array[space - 1]
            space -= 1
        array[space] = key
    return array


def ins_sort_practice(arr):
    for num in range(1, len(arr)):
        while num > 0:
            if arr[num - 1] <= arr[num]:
                break
            else:
                arr[num], arr[num - 1] = arr[num - 1], arr[num]
                num -= 1
    return arr


